for each row execute function public.audit_lead_etiquetas();

create or replace function public.validar_venda_fechada() returns trigger
language plpgsql set search_path = public as $$
begin
  if new.estagio_lead = 'fechado' and (new.nome_lead is null or btrim(new.nome_lead) = '' or new.valor is null or new.valor <= 0) then
    raise exception 'Venda fechada exige nome e valor maior que zero.' using errcode='23514';
  end if;
  if tg_op = 'INSERT' or new.observacao_vendedor is distinct from old.observacao_vendedor then
    new.observacao_autor_id := auth.uid();
    select coalesce(nullif(btrim(nome),''), email, 'Usuário desconhecido') into new.observacao_autor_nome
      from public.profiles where id=auth.uid();
    new.observacao_autor_nome := coalesce(new.observacao_autor_nome, 'Sistema');
    new.observacao_atualizada_em := now();
  end if;
  return new;
end $$;
drop trigger if exists trg_validar_venda_fechada on public."BASE_DE_LEADS";
create trigger trg_validar_venda_fechada before insert or update on public."BASE_DE_LEADS"
for each row execute function public.validar_venda_fechada();

create or replace function public.fechar_venda(p_id_lead int4, p_nome text, p_valor numeric)
returns public."BASE_DE_LEADS" language plpgsql security invoker set search_path = public as $$
declare v_lead public."BASE_DE_LEADS";
begin
  update public."BASE_DE_LEADS" set nome_lead=btrim(p_nome), valor=p_valor, estagio_lead='fechado',
    follow_manual='inativo' where id=p_id_lead and id_empresa=1 returning * into v_lead;
  if v_lead.id is null then raise exception 'Lead não encontrado.'; end if;
  return v_lead;
end $$;
grant execute on function public.fechar_venda(int4,text,numeric) to authenticated;

