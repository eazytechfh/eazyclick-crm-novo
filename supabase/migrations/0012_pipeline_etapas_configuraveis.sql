grant select, insert, update, delete on public.pipeline_etapas to authenticated;
grant usage, select on sequence public.pipeline_etapas_id_seq to authenticated;

create or replace function public.pipeline_etapas_set_updated_at()
returns trigger language plpgsql set search_path = public as $$
begin new.updated_at := now(); return new; end; $$;
drop trigger if exists trg_pipeline_etapas_updated_at on public.pipeline_etapas;
create trigger trg_pipeline_etapas_updated_at before update on public.pipeline_etapas
for each row execute function public.pipeline_etapas_set_updated_at();

create or replace function public.pipeline_etapa_protegida(p_slug text)
returns boolean language sql immutable as $$
  select p_slug in ('oportunidade','em_qualificacao','em_negociacao','follow_up');
$$;

create or replace function public.bloquear_alteracao_etapa_protegida()
returns trigger language plpgsql set search_path = public as $$
begin
  if public.pipeline_etapa_protegida(old.slug) and (
    new.slug is distinct from old.slug
    or new.nome is distinct from old.nome
    or new.cor is distinct from old.cor
    or new.ordem is distinct from old.ordem
    or new.is_inicial is distinct from old.is_inicial
  ) then
    raise exception 'Esta etapa e protegida por automacoes e nao pode ser alterada.' using errcode='42501';
  end if;
  return new;
end; $$;
drop trigger if exists trg_bloquear_alteracao_etapa_protegida on public.pipeline_etapas;
create trigger trg_bloquear_alteracao_etapa_protegida before update on public.pipeline_etapas
for each row execute function public.bloquear_alteracao_etapa_protegida();

-- Remove somente CHECK constraints que efetivamente mencionam estagio_lead.
do $$ declare r record;
begin
  for r in
    select c.conname from pg_constraint c
    join pg_class t on t.oid = c.conrelid join pg_namespace n on n.oid = t.relnamespace
    where n.nspname = 'public' and t.relname = 'BASE_DE_LEADS' and c.contype = 'c'
      and position('estagio_lead' in lower(pg_get_constraintdef(c.oid))) > 0
  loop execute format('alter table public."BASE_DE_LEADS" drop constraint %I', r.conname); end loop;
end $$;

create or replace function public.validar_estagio_lead()
returns trigger language plpgsql set search_path = public as $$
begin
  if new.estagio_lead is null or not exists (
    select 1 from public.pipeline_etapas e where e.slug = new.estagio_lead
  ) then raise exception 'Etapa do pipeline inexistente: %', coalesce(new.estagio_lead, '(vazia)') using errcode='23503';
  end if;
  return new;
end; $$;
drop trigger if exists trg_validar_estagio_lead on public."BASE_DE_LEADS";
create trigger trg_validar_estagio_lead before insert or update of estagio_lead
on public."BASE_DE_LEADS" for each row execute function public.validar_estagio_lead();

create or replace function public.impedir_exclusao_etapa_em_uso()
returns trigger language plpgsql set search_path = public as $$
begin
  if public.pipeline_etapa_protegida(old.slug) then
    raise exception 'Esta etapa e protegida por automacoes e nao pode ser excluida.' using errcode='42501';
  end if;
  if exists (select 1 from public."BASE_DE_LEADS" where estagio_lead = old.slug) then
    raise exception 'Esta etapa possui leads. Mova-os antes de excluir.' using errcode='23503';
  end if;
  if old.is_inicial then raise exception 'Defina outra etapa inicial antes de excluir esta etapa.'; end if;
  return old;
end; $$;
drop trigger if exists trg_impedir_exclusao_etapa_em_uso on public.pipeline_etapas;
create trigger trg_impedir_exclusao_etapa_em_uso before delete on public.pipeline_etapas
for each row execute function public.impedir_exclusao_etapa_em_uso();

create or replace function public.reordenar_pipeline_etapas(p_ids bigint[])
returns void language plpgsql security definer set search_path = public as $$
declare v_total integer;
begin
  if public.get_my_cargo() not in ('admin_master','admin','gerente') then raise exception 'Sem permissao para reordenar etapas.' using errcode='42501'; end if;
  select count(*) into v_total from public.pipeline_etapas;
  if cardinality(p_ids) <> v_total or (select count(distinct x) from unnest(p_ids) x) <> v_total
    or exists (select 1 from unnest(p_ids) x where not exists (select 1 from public.pipeline_etapas e where e.id=x))
  then raise exception 'A sequencia deve conter todas as etapas exatamente uma vez.'; end if;
  set constraints pipeline_etapas_ordem_key deferred;
  update public.pipeline_etapas e set ordem = u.ordem - 1
  from unnest(p_ids) with ordinality u(id, ordem) where e.id=u.id;
end; $$;
revoke execute on function public.reordenar_pipeline_etapas(bigint[]) from public, anon;
grant execute on function public.reordenar_pipeline_etapas(bigint[]) to authenticated;

