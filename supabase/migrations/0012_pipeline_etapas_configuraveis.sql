-- Etapas configuraveis do pipeline (instalacao single-tenant).
create table if not exists public.pipeline_etapas (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  nome text not null,
  cor text not null,
  ordem integer not null,
  is_inicial boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint pipeline_etapas_slug_check check (slug ~ '^[a-z0-9]+(?:_[a-z0-9]+)*$' and length(slug) <= 60),
  constraint pipeline_etapas_nome_check check (length(btrim(nome)) between 1 and 60),
  constraint pipeline_etapas_cor_check check (cor ~ '^#[0-9A-Fa-f]{6}$'),
  constraint pipeline_etapas_ordem_check check (ordem >= 0),
  constraint pipeline_etapas_ordem_key unique (ordem) deferrable initially immediate
);

create unique index if not exists pipeline_etapas_apenas_uma_inicial
  on public.pipeline_etapas (is_inicial) where is_inicial;

